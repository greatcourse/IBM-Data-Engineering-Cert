"""
This module provides utility functions for mathematical operations.
"""

def square(number: float) -> float:
    """
    Returns the square of the given number.

    Args:
        number (float): The number to be squared.

    Returns:
        float: The squared result.
    """
    return number ** 2

def double(number: float) -> float:
    """
    Returns twice the given number.

    Args:
        number (float): The number to be doubled.

    Returns:
        float: The doubled result.
    """
    return number * 2